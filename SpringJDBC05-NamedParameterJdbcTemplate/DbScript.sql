CREATE TABLE "SYSTEM"."BOOKS_INFO" 
   (	"BOOKID" NUMBER(10,0) NOT NULL ENABLE, 
	"BOOKNAME" VARCHAR2(20 BYTE), 
	"CATEGORY" VARCHAR2(20 BYTE), 
	"AUTHOR" VARCHAR2(20 BYTE), 
	"PRICE" VARCHAR2(20 BYTE), 
	"STATUS" VARCHAR2(20 BYTE), 
	 CONSTRAINT "BOOKS_INFO_PK" PRIMARY KEY ("BOOKID"));
	 
	 
	 CREATE SEQUENCE  BOOKID_SEQ MINVALUE 1000 MAXVALUE 10000 INCREMENT BY 1 START WITH 1000 CACHE 20 NOORDER  NOCYCLE ;
	 

	 